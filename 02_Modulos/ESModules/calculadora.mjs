const sumar = (a, b) => {
  return a + b
}

const restar = (a, b) => {
  return a - b
}

const multiplicar = (a, b) => {
  return a * b
}

const dividir = (a, b) => {
  return a / b
}

const modulo = (a, b) => {
  return a % b
}

export const calculadora = {
  sumar,
  restar,
  multiplicar,
  dividir,
  modulo,
}
